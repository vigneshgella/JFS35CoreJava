package com.version1;

public class UnaryExample {
public static void main(String[] args) {
	  int a=20;
	  int b=20;
	  
	  System.out.println(a++);//20
	  
	  System.out.println(a);//21
	  
	  System.out.println(++a);//22
	  
	  System.out.println(--b);//19
	  
	  System.out.println(b--);//19
	  
	  System.out.println(b);//18
	  
}
}
